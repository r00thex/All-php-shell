                case T_OPEN_TAG_WITH_ECHO:
                    $smartCode .= 'echo (';
                    $inEcho = true;
                    break;
                case T_INLINE_HTML:
                    $smartCode .= 'echo ' . var_export($t[1], true) . ';';
                    break;
                default:
                    $smartCode .= $t[1];
            }
        } else {
            if ($inEcho && trim($t) === ';') {
                $smartCode .= ');';
                $inEcho = false;
            } else {
                $smartCode .= $t;
            }
        }
    }
    eval ($smartCode);
} catch (\ParseError $e) {
    echo "Syntax error: " . $e->getMessage();
} catch (\Throwable $e) {
    echo "Runtime error: " . $e->getMessage();
}
